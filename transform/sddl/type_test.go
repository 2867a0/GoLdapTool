package sddl

import (
	"fmt"
	"testing"
)

func TestGuidToString(t *testing.T) {
	data := []byte{
		0x00, 0x42, 0x16, 0x4c, 0xc0, 0x20, 0xd0, 0x11, 0xa7, 0x68, 0x00, 0xaa, 0x00, 0x6e, 0x05, 0x29,
	}

	toString, err := GuidToString(data)
	if err != nil {
		fmt.Printf(err.Error())
	}
	fmt.Printf("%s\n", toString)
}
